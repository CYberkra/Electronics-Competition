def generate_hex(output_path="font_8x16.hex"):
    """Generate font ROM hex file."""
    lines = []
    for code in range(0x20, 0x80):
        glyph = FONT_8X16.get(code, [0x00] * 16)
        for row_data in glyph:
            lines.append(f"{row_data:02X}")

    with open(output_path, "w") as f:
        f.write("\n".join(lines) + "\n")

    print(f"Generated {output_path}: {len(lines)} bytes "
          f"({len(lines)//16} chars × 16 rows)")


if __name__ == "__main__":
    generate_hex()
